#include <stdlib.h>
#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	str_len;
	size_t	i;
	char	*dest;

	str_len = ft_strlen(s) + 1;
	dest = malloc(str_len);
	if (!dest)
		return (NULL);
	i = 0;
	while (s[i])
	{
		dest[i] = s[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
