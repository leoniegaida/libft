#include <unistd.h>

void	ft_putendl_fd(char *s, int fd)
{
	int		i;
	char	newline;

	i = 0;
	newline = '\n';
	while (s[i])
	{
		write(fd, &s[i], 1);
		i++;
	}
	write (fd, &newline, 1);
}
